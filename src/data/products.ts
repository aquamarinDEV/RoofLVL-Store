export function findProductBySlug(slug: string): Product | undefined {
  return products.find((p) => p.slug === slug)
}

export function findProductsByCategory(categorySlug: CategorySlug): Product[] {
  return products.filter((p) => p.categorySlug === categorySlug)
}

export function findProductsByCategorySlugs(slugs: CategorySlug[]): Product[] {
  return products.filter((p) => slugs.includes(p.categorySlug))
}

const CATEGORIES_WITH_IMAGES: CategorySlug[] = ['cuie-in-banda', 'cuie-finisaj', 'aluband', 'coame']

function shuffle<T>(arr: T[]): T[] {
  const out = [...arr]
  for (let i = out.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [out[i], out[j]] = [out[j], out[i]]
  }
  return out
}

export function getMixedProductsWithImages(limit = 12): Product[] {
  const byCategory = CATEGORIES_WITH_IMAGES.flatMap((slug) => findProductsByCategory(slug))
  return shuffle(byCategory).slice(0, limit)
}

